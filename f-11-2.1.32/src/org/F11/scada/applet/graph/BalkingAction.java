package org.F11.scada.applet.graph;

/**
 * ボーキングするアクションのインターフェイスです。
 * @author maekawa
 */
interface BalkingAction {
	/**
	 * ボーキングする場合はtrueをしない場合はfalseを返します。
	 * @param obj 判定材料のオブジェクト
	 * @return ボーキングする場合はtrueをしない場合はfalseを返します。
	 */
	boolean isBalk(Object obj);

	/**
	 * ボーキングの判定材料のオブジェクトを設定します。
	 * @param obj ボーキングの判定材料のオブジェクト
	 */
	void setBalk(Object obj);
}
